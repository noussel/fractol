#include "libft.h"
#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	unsigned char	cc;
	unsigned int	i;

	i = 0;
	cc = (unsigned char)c;
	while (s[i] != '\0')
	{
		if (s[i] == cc)
			return ((char *)&s[i]);
		i++;
	}
	if (s[i] == cc)
		return ((char *)&s[i]);
	return (NULL);
}
